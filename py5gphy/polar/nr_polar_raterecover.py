def ratemrecover_polar(inbits, K, N, iBIL):
    """ does Polar block rate match recovery TS38.212 5.4.1
    outbits = ratemrecover_polar(inbits, K, N, iBIL)
    input:
        inbits: E length input bits
        K: information bits length
        N: rate match input length
        iBIL: interleaving coded bits or not. iBIL=1 for UL, 0 for DL
    output:
        outbits: N length bits
    """
    E = inbits.size

    assert E <= 8192
    if iBIL:  #UL
        assert not((K > 25) and (K <=30))  
        assert K > 18
    
    # Channel deinterleaving, Section 5.4.1.3
    if iBIL:  #Specified for uplink only
        inE = _iBIL_deinterl(inbits, E)
        pass
    else:
        inE = inbits

    # Bit selection, Section 5.4.1.2
    outN = np.zeros(N, 'i1')
    if E >= N:
        outN = inE[0:N]
    else:
        if (K/E) <= (7/16): #puncturing, put at the end
            outN[N-E:N] = inE
        else:  #shortening, put at the start
            outN[0:E] = inE
    
    #Sub-block deinterleaving, Section 5.4.1.1
    # Table 5.4.1.1-1: Sub-block interleaver pattern
    pi = [0,1,2,4, 3,5,6,7, 8,16,9,17, 10,18,11,19,
          12,20,13,21, 14,22,15,23, 24,25,26,28, 27,29,30,31]
    outbits = np.zeros(N, 'i1')
    for n in range(N):
        m = (32 * n) // N
        jn = pi[m] * (N // 32) + (n % (N // 32))
        outbits[jn] = outN[n]
    
    return outbits

def _iBIL_deinterl(inbits, E):
    # T is smallest integer such that T(T+1)/2>=E
    T = math.ceil((-1 + math.sqrt(1+8*E))/2)

    # create table
    vtable = np.zeros((T,T),'i1')
    k = 0
    for m in range(T):
        for n in range(T-m):
            if k < E:
                vtable[m,n] = 1
            k +=1
    
    #Write input to buffer column-wise
    V = -1 * np.ones((T, T), 'i1') 
    k = 0
    for n in range(T):
        for m in range(T-n):
            if (k < E) and vtable[m,n]:
                V[m,n] = inbits[k]
                k += 1
    
    #read outrow-wise
    outd = np.zeros(E, 'i1')
    k = 0
    for m in range(T):
        for n in range(T-m):
            if V[m,n] != -1:
                outd[k] = V[m,n]
                k += 1
    
    return outd


