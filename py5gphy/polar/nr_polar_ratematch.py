# -*- coding:utf-8 -*-

import numpy as np
import math

def ratematch_polar(inbits, K, E, iBIL):
    """ does Poar block rate match TS38.212 5.4.1
    outbits = ratematch_polar(inbits, K, E, iBIL)
    input:
        inbits: N length input bits
        K: information bits length
        E: rate match output length
        iBIL: interleaving coded bits or not. iBIL=1 for UL, 0 for DL
    output:
        outbits: E length bits
    """

    #validate input
    assert E <= 8192
    if iBIL:  #UL
        assert (K in range(18,26)) or (K > 30)
        
    N = inbits.size
    assert N in [2 ** 5, 2**6, 2**7, 2**8, 2**9, 2**10]
    
    #sub block interleaving 5.4.1.1
    # Table 5.4.1.1-1: Sub-block interleaver pattern
    pi = [0,1,2,4, 3,5,6,7, 8,16,9,17, 10,18,11,19,
          12,20,13,21, 14,22,15,23, 24,25,26,28, 27,29,30,31]
    yn = np.zeros(N, 'i1')
    for n in range(N):
        m = (32 * n) // N
        jn = pi[m] * (N // 32) + (n % (N // 32))
        yn[n] = inbits[jn]

    #bit selection 5.4.1.2
    ek = np.zeros(E, 'i1') 
    if E >= N: #repetition
        for k in range(E):
            ek[k] = yn[k % N]
    else:
        if (K/E) <= (7/16): #puncturing
            for k in range(E):
                ek[k] = yn[k+N-E]
        else: #shortening
            ek = yn[0:E]

    # interleaving of coded bits 5.4.1.3
    if iBIL:
        # T is smallest integer such that T(T+1)/2>=E
        T = math.ceil((-1 + math.sqrt(1+8*E))/2)

        # Write input to buffer row-wise, -1 means NULL
        V = -1 * np.ones((T, T), 'i1') 
        k = 0
        for m in range(T):
            for n in range(T-m):
                if k < E:
                    V[m, n] = ek[k]
                k += 1

        #Read output from buffer column-wise
        fk = np.zeros(E, 'i1')
        k = 0
        for n in range(T):
            for m in range(T-n):
                if V[m,n] != -1:
                    fk[k] = V[m, n]
                    k += 1
    else:
        fk = ek
    
    return fk

